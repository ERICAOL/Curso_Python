

def calcular_area_quadrado(lado):
    area = lado ** 2
    return area

def calcular_perimetro_quadrado(lado):
    perimetro = 4 * lado
    return perimetro

def calcular_area_triangulo(base, altura):
    area = 0.5 * base * altura
    return area

def calcular_area_retangulo(largura, altura):
    area = largura * altura
    return area
